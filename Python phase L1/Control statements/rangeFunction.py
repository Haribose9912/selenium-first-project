#range() function

print(list(range(10))) # will get from 0 to 9
print(list(range(5,10))) # will get from 5 to 9

# print only odd number in 1 to 10 numbers
print(list(range(1,10,2)))

# print only even numbers
print(list(range(0,10,2)))

print(list(range(10,0,-1))) #decending order
print(list(range(0,10,1))) # ascending order

